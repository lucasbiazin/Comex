
public class Categoria {

	int id;
	String nome;
	Status status;
	
	
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getNome() {
		return nome;
	}
	public void setNome(String nome) {
		this.nome = nome;
	}
	public Status isStatus() {
		return status;
	}
	public void setStatus(Status status) {
		this.status = status;
	}
	
}
